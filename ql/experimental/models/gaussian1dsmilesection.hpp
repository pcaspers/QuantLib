/*! \file gaussian1dsmilesection.hpp
    \brief smile section generated by a gaussian 1d model
*/

#ifndef quantlib_gaussian1dsmilesection_hpp
#define quantlib_gaussian1dsmilesection_hpp

#include <ql/termstructures/volatility/smilesection.hpp>
#include <ql/indexes/swapindex.hpp>
#include <ql/indexes/iborindex.hpp>
#include <ql/experimental/models/gaussian1dmodel.hpp>
#include <ql/experimental/models/gaussian1dswaptionengine.hpp>
#include <ql/experimental/models/gaussian1dcapfloorengine.hpp>

namespace QuantLib {

/*! smile section based on a gaussian 1d model instance
    if curves are attached to the swap or ibor index, these are used to adjust
    the model's yield term structure, if not the model's yield term structure
    is used directly */

class Gaussian1dSmileSection : public SmileSection {
  public:
    Gaussian1dSmileSection(const Date &fixingDate,
                           const boost::shared_ptr<SwapIndex> swapIndex,
                           const boost::shared_ptr<Gaussian1dModel> &model,
                           const boost::shared_ptr<Gaussian1dSwaptionEngine>
                               swaptionEngine = NULL);
    Gaussian1dSmileSection(const Date &fixingDate,
                           const boost::shared_ptr<IborIndex> iborIndex,
                           const boost::shared_ptr<Gaussian1dModel> &model,
                           const boost::shared_ptr<Gaussian1dCapFloorEngine>
                               swaptionEngine = NULL);

    // the minimum strike is zero only because we are returning a lognormal section
    Real minStrike() { return 0.0; }
    Real maxStrike() { return QL_MAX_REAL; }

    Real atmLevel();
    Real optionPrice(Rate strike, Option::Type = Option::Call, Real discount=1.0);

  protected:
    Real volatilityImpl(Rate strike);

  private:
    Real atm_, annuity_;
    Date fixingDate_;
    boost::shared_ptr<SwapIndex> swapIndex_;
    boost::shared_ptr<IborIndex> iborIndex_;
    boost::shared_ptr<Gaussian1dModel> model_;
    boost::shared_ptr<PricingEngine> engine_;

};
}

#endif
